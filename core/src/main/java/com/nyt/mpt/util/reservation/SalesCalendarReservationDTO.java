/**
 * DTO for sales calendar
 */
package com.nyt.mpt.util.reservation;

import java.util.Date;
import java.util.List;
import java.util.Set;

import com.nyt.mpt.domain.LineItemTarget;

/**
 * @author amandeep.singh
 *
 */
public class SalesCalendarReservationDTO {
	private List<Long> productIds;
	private Date startDate;
	private Date endDate;
	private String productType;
	private String countries;
	private String dateString;
	private String weekDay;
	private Set<LineItemTarget> lineItemTargeting;
	private List<Long> salesTargetIds;
	
	public List<Long> getProductIds() {
		return productIds;
	}
	public void setProductIds(List<Long> productIds) {
		this.productIds = productIds;
	}
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public String getProductType() {
		return productType;
	}
	public void setProductType(String productType) {
		this.productType = productType;
	}
	public Set<LineItemTarget> getLineItemTargeting() {
		return lineItemTargeting;
	}
	public void setLineItemTargeting(Set<LineItemTarget> lineItemTargeting) {
		this.lineItemTargeting = lineItemTargeting;
	}
	public String getCountries() {
		return countries;
	}
	public void setCountries(String countries) {
		this.countries = countries;
	}
	public String getDateString() {
		return dateString;
	}
	public void setDateString(String dateString) {
		this.dateString = dateString;
	}
	public String getWeekDay() {
		return weekDay;
	}
	public void setWeekDay(String weekDay) {
		this.weekDay = weekDay;
	}
	public List<Long> getSalesTargetIds() {
		return salesTargetIds;
	}
	public void setSalesTargetIds(List<Long> salesTargetIds) {
		this.salesTargetIds = salesTargetIds;
	}
}
