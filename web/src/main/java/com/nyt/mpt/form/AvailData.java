package com.nyt.mpt.form;

/**
 * Used to ObjectMapper to map avails json data
 * 
 * @author rakesh.tewari
 */
public class AvailData {

	private long lineItemID;

	private String avail;

	private String capacity;

	public void setLineItemID(final long lineItemID) {
		this.lineItemID = lineItemID;
	}

	public long getLineItemID() {
		return lineItemID;
	}

	public void setAvail(final String avail) {
		this.avail = avail;
	}

	public String getAvail() {
		return avail;
	}

	public void setCapacity(final String capacity) {
		this.capacity = capacity;
	}

	public String getCapacity() {
		return capacity;
	}
}
