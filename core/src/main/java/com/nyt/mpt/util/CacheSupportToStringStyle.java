package com.nyt.mpt.util;

import org.apache.commons.lang.builder.ToStringStyle;

/**
 * This class used for CacheSupport.
 * 
 * @author surendra.singh
 * 
 */
public class CacheSupportToStringStyle extends ToStringStyle {

	private static final long serialVersionUID = 1L;

	public CacheSupportToStringStyle() {
		setUseIdentityHashCode(false);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.apache.commons.lang.builder.ToStringStyle#setUseIdentityHashCode(
	 * boolean)
	 */
	@Override
	protected void setUseIdentityHashCode(boolean useIdentityHashCode) {
		super.setUseIdentityHashCode(false);
	}
}
