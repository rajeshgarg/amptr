package com.nyt.mpt.form;

/**
 * This class is used for advertiser info
 * 
 * @author amandeep.singh
 * 
 */
public class AdvertiserForm {

	private long advertiserId;

	private String advertiserName;

	private AddressForm addressForm;

	public long getAdvertiserId() {
		return advertiserId;
	}

	public void setAdvertiserId(final long advertiserId) {
		this.advertiserId = advertiserId;
	}

	public String getAdvertiserName() {
		return advertiserName;
	}

	public void setAdvertiserName(final String advertiserName) {
		this.advertiserName = advertiserName != null ? advertiserName.trim() : advertiserName;
	}

	public AddressForm getAddressForm() {
		return addressForm;
	}

	public void setAddressForm(final AddressForm addressForm) {
		this.addressForm = addressForm;
	}

}