package com.nyt.mpt.util.filter;

import java.util.Date;

/**
 * This class is used to create Date Range Filter Criteria
 * 
 * @author surendra.singh
 * 
 */
public class DateRangeFilterCriteria extends FilterCriteria {

	private Date searchStringFrom;
	
	private Date searchStringTo;

	public DateRangeFilterCriteria() {
		super();
	}

	public Date getSearchStringFrom() {
		return searchStringFrom;
	}

	public void setSearchStringFrom(Date searchStringFrom) {
		this.searchStringFrom = searchStringFrom;
	}

	public Date getSearchStringTo() {
		return searchStringTo;
	}

	public void setSearchStringTo(Date searchStringTo) {
		this.searchStringTo = searchStringTo;
	}
}
