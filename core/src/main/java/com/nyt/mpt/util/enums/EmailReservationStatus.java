package com.nyt.mpt.util.enums;

/**
 * This <code>EmailReservationStatus</code> enum contains all the status for the Email Reservation
 * 
 * @author amandeep.singh
 */
public enum EmailReservationStatus {

	/**
	 * Represent status of Email Reservation is "Over-booked"
	 */
	OVERBOOKED {

		@Override
		public String getDisplayName() {
			return "Overbooked";
		}
	},
	/**
	 * Represent status of Email Reservation is "Reserved"
	 */
	RESERVED {

		@Override
		public String getDisplayName() {
			return "Reserved";
		}
	},
	/**
	 * Represent status of Email Reservation is "Sold"
	 */
	SOLD {

		@Override
		public String getDisplayName() {
			return "Sold";
		}
	},
	/**
	 * Represent status of Email Reservation is "Available"
	 */
	AVAILABLE {

		@Override
		public String getDisplayName() {
			return "Available";
		}
	};
	public abstract String getDisplayName();
}
