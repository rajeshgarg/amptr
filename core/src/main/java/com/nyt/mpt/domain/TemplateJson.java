package com.nyt.mpt.domain;

/**
 * Used by object mapper to map json data
 * 
 * @author rakesh.tewari
 */
public class TemplateJson {
	private String tokenName;
	private String proposalHead;
	private String proposalAttribute;
	private String rowNum;
	private String colNum;
	private Long attributesId;
	private Long fontSize;

	private Long selectedKey;// used when read data from data base

	public String getTokenName() {
		return tokenName;
	}

	public void setTokenName(final String tokenName) {
		this.tokenName = tokenName;
	}

	public String getProposalHead() {
		return proposalHead;
	}

	public void setProposalHead(final String proposalHead) {
		this.proposalHead = proposalHead;
	}

	public String getProposalAttribute() {
		return proposalAttribute;
	}

	public void setProposalAttribute(final String proposalAttribute) {
		this.proposalAttribute = proposalAttribute;
	}

	public String getRowNum() {
		return rowNum;
	}

	public void setRowNum(final String rowNum) {
		this.rowNum = rowNum;
	}

	public String getColNum() {
		return colNum;
	}

	public void setColNum(final String colNum) {
		this.colNum = colNum;
	}

	public Long getSelectedKey() {
		return selectedKey;
	}

	public void setSelectedKey(final Long selectedKey) {
		this.selectedKey = selectedKey;
	}

	public Long getAttributesId() {
		return attributesId;
	}

	public void setAttributesId(final Long attributesId) {
		this.attributesId = attributesId;
	}

	public Long getFontSize() {
		return fontSize;
	}

	public void setFontSize(Long fontSize) {
		this.fontSize = fontSize;
	}
}