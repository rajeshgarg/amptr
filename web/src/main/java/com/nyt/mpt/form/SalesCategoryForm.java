package com.nyt.mpt.form;

/**
 * This form bean is used for Sales Category information
 * 
 * @author surendra.singh
 * 
 */
public class SalesCategoryForm {

	private long salesCategoryId;

	private String salesCategoryName;

	public String getSalesCategoryName() {
		return salesCategoryName;
	}

	public void setSalesCategoryName(final String salesCategoryName) {
		this.salesCategoryName = salesCategoryName != null ? salesCategoryName.trim() : salesCategoryName;
	}

	public long getSalesCategoryId() {
		return salesCategoryId;
	}

	public void setSalesCategoryId(final long salesCategoryId) {
		this.salesCategoryId = salesCategoryId;
	}
}
