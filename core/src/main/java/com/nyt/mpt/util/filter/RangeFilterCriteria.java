package com.nyt.mpt.util.filter;

/**
 * Provide Range FilterCriteria
 * 
 * @author surendra.singh
 * 
 */
public class RangeFilterCriteria extends FilterCriteria {

	private String searchStringFrom;

	private String searchStringTo;

	public String getSearchStringFrom() {
		return searchStringFrom;
	}

	public void setSearchStringFrom(String searchStringFrom) {
		this.searchStringFrom = searchStringFrom;
	}

	public String getSearchStringTo() {
		return searchStringTo;
	}

	public void setSearchStringTo(String searchStringTo) {
		this.searchStringTo = searchStringTo;
	}
}
